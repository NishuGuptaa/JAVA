public class a4CharDataType {
    public static void main(String[] args){
        char var = 'G';
        System.out.println(var);
        char ch = 65;
        System.out.println(ch);
        char hash = '#';
        System.out.println(hash);
        char dig = 48;
        System.out.println(dig);
        char hindi = '\u0905';
        System.out.println(hindi);
    }
}
